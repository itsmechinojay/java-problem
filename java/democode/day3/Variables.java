package com.accenture.democode.day3;

import java.time.LocalDate;

public class Variables {

    public static void main(String[] args) {

        // Datatype

        // Primitive Type
        int number = 4; // 32-bit -2,147,483,648 to 2,147,483,647
        double numberDouble = 3.143; // 64-bit
        char letter = 'a'; // 8-bits ASCII
        boolean isActive = true;

        long largeNumber = 867458534; // 64-bit
        byte numByte = 3; // 8-bits -128 to 127
        short numShort = 12; // 16-bits
        float numFloat = 12.1f; // 32-bit

        // Corresponding ref type
        Integer numberRef = 1;

        // Reference Type
        String message = "Hello World";
        LocalDate now = LocalDate.now();


    }
}
